<h5><b>Jasa Persewaan Ruangan</b></h5> <br />

Jasa persewaan ruangan termasuk dalam jenis jasa persewaan barang tak gerak yang atas penyerahannya dikenakan Pajak Pertambahan Nilai, kecuali jasa persewaan ruangan di bidang perhotelan tidak dikenakan Pajak Pertambahan Nilai.<br /><br />

Jasa di bidang perhotelan yang tidak dikenakan Pajak Pertambahan Nilai meliputi :<br />
<ul>
    <li>jasa persewaan kamar termasuk tambahannya di hotel, rumah penginapan, motel, losmen, hostel, serta fasilitas yang terkait dengan kegiatan perhotelan untuk tamu yang menginap;</li>
    <li>jasa persewaan ruangan untuk kegiatan acara atau pertemuan di hotel, rumah penginapan, motel, losmen dan hostel.</li>
</ul>
Jasa persewaan ruangan yang atas penyerahannya dikenakan Pajak Pertambahan Nilai selain jasa di bidang perhotelan antara lain :<br>
<ul>
    <li> jasa persewaan ruangan untuk perkantoran;</li>
    <li> jasa persewaan ruangan untuk tempat usaha atau pertokoan;</li>
    <li> jasa persewaan ruangan untuk tempat tinggal, apartemen, flat;</li>
    <li> jasa persewaan ruangan untuk pertemuan (convention hall);</li>
    <li> dan lain-lain jasa persewaan ruangan sejenisnya.</li>
</ul>
<h5><b>Yang Perlu Dilakukan Oleh Pengusaha Jasa Persewaan Ruangan</b> </h5> 

Pengusaha Jasa Persewaan Ruangan selain di bidang perhotelan yang selama satu tahun buku melakukan penyerahan jasa persewaan ruangan dengan nilai peredaran bruto lebih dari Rp. 600.000.000,00 (enam ratus juta rupiah), wajib :<br /><br />
<ul>
    <li> mendaftarkan diri pada Kantor Pelayanan Pajak setempat untuk dikukuhkan menjadi Pengusaha Kena Pajak;</li>
    <li> memungut, menyetor dan melaporkan Pajak Pertambahan Nilai yang terutang.</li>
</ul>
<h5><b>Menghitung PPN Atas Jasa Sewa Ruangan</b> </h5>
<ul>
    <li> Dasar Pengenaan Pajak atas sewa ruangan adalah jumlah penggantian atau imbalan atau nilai sewa ruangan dalam keadaan kosong yang diminta atau seharusnya diminta oleh PKP yang menyewakan ruangan, tidak termasuk service charge.<br />
    PPN yang terutang adalah : 10% x Jumlah Nilai Sewa</li>
    <li>Dasar Pengenaan Pajak atas service charge adalah:
        <ol>
		<li>Penggantian yakni sebesar nilai tagihan</li> 
        <li>jumlah service charge yang diminta oleh Pengusaha Kena Pajak yang menyewakan ruangan.<br />
        Service charge, yaitu imbalan atas jasa yang menyebabkan ruangan yang disewa tersebut dapat dihuni sesuai dengan tujuan yang diinginkan oleh penyewa. <br />
        Service charge dapat terdiri dari biaya listrik, air, keamanan, kebersihan, dan biaya administrasi.<br />
		PPN yang terutang adalah 10% x Jumlah Service Charge. </ol></li>
</ul>
<h5><b>Pengkreditan Pajak Masukan</b> </h5>
<ul>
    <li>Bagi PKP yang menyewakan ruangan dapat mengkreditkan PPN (Pajak Masukan) yang dibayar atas perolehan barang dan jasa untuk pengoperasian gedung atau ruangan yang disewakan.</li> 
    <li>Bagi Pihak yang menyewa ruangan:
        <ol><li> apabila penyewa adalah PKP, maka PPN (Pajak Masukan) yang dibayar atas ruangan yang disewa merupakan Pajak Masukan yang dapat dikreditkan, sepanjang Faktur Pajaknya memenuhi ketentuan pembuatan Faktur Pajak.</li>
